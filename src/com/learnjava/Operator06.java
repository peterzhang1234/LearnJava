package com.learnjava;
/*
    关系运算符
 */
public class Operator06 {
    public static void main(String[] args){
        //定义变量
        int i=10,j=20,k=10;
        //==
        System.out.println(i == j);
        System.out.println(i == k);
        System.out.println("--------");

        //=！
        System.out.println(i != j);
        System.out.println(i != k);
        System.out.println("-------");

        //>
        System.out.println(i > j);
        System.out.println(i > k);
        System.out.println("-------");

        //>=
        System.out.println(i >= j);
        System.out.println(i >= k);
        System.out.println("--------");

    }
}
