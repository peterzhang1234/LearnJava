package com.learnjava;

public class Operator01 {
    public static void main(String[] args){
        int a=6,b=4;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a / b);
        System.out.println(a % b);

        //整数相除只能得到整数，想要得到小数，必须有浮点数的参与
        System.out.println(6.0/4);
    }
}
